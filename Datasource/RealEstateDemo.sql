CREATE OR ALTER VIEW dbo.vPropertySummary AS
SELECT p.PropertyID, l.City, l.State, p.PropertyType, p.Bedrooms, p.Bathrooms, p.SizeSqFt,
       p.PricePerSqFt, p.ListPrice, p.Status
FROM dbo.Properties p
JOIN dbo.Locations l ON p.LocationID = l.LocationID;
GO

CREATE OR ALTER VIEW dbo.vSalesPerformance AS
SELECT t.TransactionID, t.TransactionDate, a.AgentName, c.CustomerName, l.City, l.State,
       t.TransactionType, t.SaleAmount, t.RentAmount, t.CommissionPct
FROM dbo.Transactions t
JOIN dbo.Listings ls ON t.ListingID = ls.ListingID
JOIN dbo.Properties p ON ls.PropertyID = p.PropertyID
JOIN dbo.Locations l ON p.LocationID = l.LocationID
JOIN dbo.Agents a ON ls.AgentID = a.AgentID
JOIN dbo.Customers c ON t.CustomerID = c.CustomerID;
GO


